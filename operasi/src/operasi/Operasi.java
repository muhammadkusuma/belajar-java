package operasi;
public class Operasi {
    public static void main(String[] args) {
        int x=5;
        int y=8;
        int jumlah,kurang,kali,bagi;
        jumlah=x+y;
        kurang=x-y;
        kali=x*y;
        bagi=x/y;
        System.out.println("X+Y = "+jumlah);
        System.out.println("X-Y = "+kurang);
        System.out.println("X*Y = "+kali);
        System.out.println("X/Y = "+bagi);
    }
    
}
